package com.anz.common.compute;

/**
 * @author root
 *
 */
public class ComputeInfo {
	
	String messageFlow;
	
	String application;
	
	String broker;
	
	String computeName;
	
	

	/**
	 * @return the computeName
	 */
	public String getComputeName() {
		return computeName;
	}

	/**
	 * @param computeName the computeName to set
	 */
	public void setComputeName(String computeName) {
		this.computeName = computeName;
	}

	/**
	 * @return the messageFlow
	 */
	public String getMessageFlow() {
		return messageFlow;
	}

	/**
	 * @param messageFlow the messageFlow to set
	 */
	public void setMessageFlow(String messageFlow) {
		this.messageFlow = messageFlow;
	}

	/**
	 * @return the application
	 */
	public String getApplication() {
		return application;
	}

	/**
	 * @param application the application to set
	 */
	public void setApplication(String application) {
		this.application = application;
	}

	/**
	 * @return the broker
	 */
	public String getBroker() {
		return broker;
	}

	/**
	 * @param broker the broker to set
	 */
	public void setBroker(String broker) {
		this.broker = broker;
	}
	
	
	

}
